// # Apa itu Web Service ?
// Web Service adalah layanan yang memungkinkan dua aplikasi yang berbeda untuk saling berkomunikasi melalui internet
// Interpretasinya sendiri, web service ini bukanlah sebuah tampilan website, tapi merupakan jembatan data antar sistem
// contoh: API BNI, API POST, API E-Wallet, dsb..
// Semua proses itu tidak tampak oleh pengguna, tetapi data berpindah antar sistem menggunakan Web Service

// Client -> Mengirim request (misal: minta data karyawan)
// Server -> Memberi response (misal: data JSON)

// GET -> mengambil data
// POST -> mengirim data baru
// PUT -> mengubah data
// DELETE -> menghapus data
// Semua komunikasi menggunakan format JSON